#!/usr/bin/env python3
"""女性作品题材分布(题目多标签,词牌剔除;对 data/out/women_poems.csv)。
注意:women_poems 含 A/B 两层,唐宋女性作品有少量重复计入(~9%),占比解读时注明。"""
import csv
import re
from collections import defaultdict
from pathlib import Path

from opencc import OpenCC

T2S = OpenCC("t2s")
OUT = Path(__file__).resolve().parent.parent / "data" / "out"

TUNES = ['凤凰台上忆吹箫', '减字木兰花', '摊破浣溪沙', '桃源忆故人', '春从天上来', '木兰花慢', '千秋岁引', '水调歌头', '八声甘州', '一剪梅', '蝶恋花', '木兰花', '玉楼春', '满庭芳', '桂枝香', '杨柳枝', '柳梢青', '梅花引', '落梅风', '一萼红', '兰陵王', '画堂春', '沁园春', '武陵春', '汉宫春', '绛都春', '探春令', '春光好', '千秋岁', '秋蕊香', '秋波媚', '夜行船', '夜游宫', '夜飞鹊', '西江月', '月华清', '望海潮', '渡江云', '满江红', '望江南', '忆旧游', '浪淘沙', '虞美人', '醉花阴', '醉花间', '浣溪沙', '鹧鸪天', '临江仙', '菩萨蛮', '卜算子', '点绛唇', '清平乐', '如梦令', '念奴娇', '水龙吟', '贺新郎', '金缕曲', '荷叶杯', '芳心苦', '买陂塘', '摸鱼儿', '忆秦娥', '忆江南', '忆王孙', '长相思', '诉衷情', '昭君怨', '绮罗香', '双双燕', '解语花', '惜红衣', '庆清朝', '宴山亭', '燕山亭', '瑞鹤仙', '眼儿媚', '青玉案', '风入松', '雨霖铃', '定风波', '行香子', '踏莎行', '蓦山溪', '小重山', '阮郎归', '生查子', '南乡子', '渔家傲', '苏幕遮', '天仙子', '雀踏枝', '声声慢', '永遇乐', '孤雁儿', '怨王孙', '好事近', '殢人娇', '庆春泽', '梦江南', '梦横塘', '梦芙蓉', '疏影', '暗香', '花犯', '六丑', '多丽', '绿意', '红情', '梦令']
TUNE = re.compile("|".join(map(re.escape, TUNES)))
CATS = {
    "四季时令": r"春|夏日|秋|冬|元夕|上元|中秋|七夕|清明|除夕|重阳|端午|寒食|立春|新年|元旦",
    "花木草虫": r"梅|兰|菊|荷|莲|柳|桃|杏|海棠|牡丹|桂|蔷薇|芙蓉|杨花|落花|草|燕|蝶|雁|莺",
    "送别寄赠": r"送|留别|寄|赠|简",
    "山水行旅": r"舟|渡|泊|途中|道中|山行|游|登|过|晚眺|望|桥|湖|江上|山中",
    "夜与独处": r"夜|独坐|独酌|无寐|不寐|晓起|枕上|灯",
    "亲情家人": r"寄外|忆外|别外|外子|寄夫|忆女|哭女|殇女|示女|示儿|哭儿|忆母|思亲|寄兄|寄弟|寄妹|寄姊|忆弟|怀弟|哭兄|哭妹|哭姊|归宁|大人|先慈|先考",
    "题画题物": r"题.{0,8}(图|画|卷|册|轴|扇)|自题|题壁|题照",
    "感怀述志": r"感怀|感事|书怀|述怀|遣怀|自遣|偶成|漫兴|即事|杂感|自况|言志",
    "唱和次韵": r"次韵|和韵|奉和|步韵|和答|^和|叠韵|依韵",
    "悼亡哭挽": r"悼|哭|挽|殇|吊",
    "闺怨宫怨": r"怨|闺情|宫词|春闺|秋闺",
    "读书论诗": r"读|书后|论诗|题.{0,6}(集|稿|诗)|作诗|课",
    "宗教方外": r"禅|佛|寺|庵|道院|仙|观音|礼佛|诵经",
    "梦": r"梦",
    "病中": r"病",
    "自伤绝命": r"绝命|自挽|临终|自伤|自叹|将死|绝笔",
}
CATS = {k: re.compile(v) for k, v in CATS.items()}


def main():
    rows = list(csv.DictReader(open(OUT / "women_poems.csv")))
    n = len(rows)
    cnt = defaultdict(int)
    nohit = 0
    for r in rows:
        t = TUNE.sub("", T2S.convert(r["title"] or ""))
        hits = [k for k, pat in CATS.items() if pat.search(t)]
        for k in hits:
            cnt[k] += 1
        if not hits:
            nohit += 1
    print(f"女性作品 {n} 首(题目多标签,词牌剔除)")
    for k, v in sorted(cnt.items(), key=lambda kv: -kv[1]):
        print(f"{k:6s} {v:5d} 首 {v/n*100:6.2f}%")
    print(f"{'无命中':6s} {nohit:5d} 首 {nohit/n*100:6.2f}%")


if __name__ == "__main__":
    main()
